	public static void countryArea(){

		Scanner sc = new Scanner(System.in);
		System.out.print("What is square area of your destination country in km2? "); 
		String dstr = sc.next();
		dstr = dstr.replace(",", ".");
		double areakm2 = Double.parseDouble(dstr);
		double areamile2 = areakm2*0.3861018768412233;
		System.out.print("In miles2 that is "+Math.floor(areamile2*100)/100+"\n");

	}

	public static void vacationPlan(){

		Scanner sc = new Scanner(System.in);
		System.out.print("How many places you traveled to : ");
		int n = sc.nextInt();
		String a[] = new String[n+5];
		System.out.print("Enter them: \n");
		for(int i = 0; i<n; i++) {System.out.print((i+1)+". ") ; a[i] = sc.next();}
		System.out.print("5 countries that you want visit in future: \n");
		for(int i = 0; i<5; i++) {System.out.print((i+1)+". ") ; a[i+n] = sc.next();}
		System.out.print("Available places to travel : \n");
		for(int i = 0; i<n+5; i++) System.out.println((i+1)+". "+a[i]);
		System.out.print("# of next place you want to travel to : ");
		int next = sc.nextInt();
		System.out.print("Thank you, Your next location is : "+a[next-1]);

	}

